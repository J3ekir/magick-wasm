/**
 * Specified the photo orientation of the image.
 */
export enum OrientationType {
    /**
     * Undefined.
     */
    Undefined,

    /**
     * Top left.
     */
    TopLeft,

    /**
     * Top right.
     */
    TopRight,

    /**
     * Bottom right.
     */
    BottomRight,

    /**
     * Bottom left.
     */
    BottomLeft,

    /**
     * Left top.
     */
    LeftTop,

    /**
     * Right top.
     */
    RightTop,

    /**
     * Right bottom.
     */
    RightBottom,

    /**
     * Left bottom.
     */
    LeftBottom,
}
