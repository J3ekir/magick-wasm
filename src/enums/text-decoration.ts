/**
 * Specifies the types of text decoration.
 */
export enum TextDecoration {
    /**
     * Undefined.
     */
    Undefined,

    /**
     * None.
     */
    None,

    /**
     * Underline.
     */
    Underline,

    /**
     * Overline.
     */
    Overline,

    /**
     * Line through.
     */
    LineThrough,
}
