/**
 * Specifies the interlace types.
 */
export enum Interlace {
    /**
     * Undefined.
     */
    Undefined,

    /**
     * No interlacing.
     */
    NoInterlace,

    /**
     * Line.
     */
    Line,

    /**
     * Plane.
     */
    Plane,

    /**
     * Partition.
     */
    Partition,

    /**
     * Gif.
     */
    Gif,

    /**
     * Jpeg.
     */
    Jpeg,

    /**
     * Png.
     */
    Png,
}
