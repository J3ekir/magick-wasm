/**
 * Specifies the pixel intensity methods.
 */
export enum PixelIntensityMethod {
    /**
     * Undefined.
     */
    Undefined,

    /**
     * Average.
     */
    Average,

    /**
     * Brightness.
     */
    Brightness,

    /**
     * Lightness.
     */
    Lightness,

    /**
     * MS.
     */
    MS,

    /**
     * Rec601Luma.
     */
    Rec601Luma,

    /**
     * Rec601Luminance.
     */
    Rec601Luminance,

    /**
     * Rec709Luma.
     */
    Rec709Luma,

    /**
     * Rec709Luminance.
     */
    Rec709Luminance,

    /**
     * RMS.
     */
    RMS
}
